#This is cut copy paste

=begin 
1. Every deletion command is a cut command in vim.
2. dw (delete word)
   (p key is used to paste the word.)
3. P(big p) is used to paste the word.
4. P is for before cursor.

..... Copy .....
1. yw (yank word)
   p or P used to used.
2. yy (yank yank) copies the whole line.
3. yo copy from cursor to the beginning of the line.
4. 5p (It will paste the content 5 times)
5. If something deletes by delete command it is available
   in memory.
6. x key is used to delete a character.
7. 14x will delete 14 characters.
8. Big X delete letter right behind the cursor.


=end
